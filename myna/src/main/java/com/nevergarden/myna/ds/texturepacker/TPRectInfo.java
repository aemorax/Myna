public class TPRectInfo {
    public Integer x;
    public Integer y;
    public Integer w;
    public Integer h;

    @Override
    public String toString() {
        return "{" +
                "x=" + x +
                ", y=" + y +
                ", w=" + w +
                ", h=" + h +
                '}';
    }
}
